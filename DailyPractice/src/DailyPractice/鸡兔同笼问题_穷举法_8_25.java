package DailyPractice;

public class 鸡兔同笼问题_穷举法_8_25 {
/**
 * 
 *2021年8月25日 下午8:02:48
 * @param args
 * 练习题: 鸡兔同笼问题(穷举法)
 * 已知:鸡兔共35只,共94只脚,那么鸡和兔各几只?
 * //穷举法
//鸡    兔
//0     35
//1     34
//2     33
//3     32
//...
//23    12
//...
//35    0
 */
	public static void main(String[] args) {
		//循环变量j,控制小鸡的个数: 0到35递增
        //循环变量t,控制兔子的个数: 35到0递减
		for(int j=0,t=35;j<=35;j++,t--) {
			if(j*2+t*4==94) //保证脚的数量是94
				System.out.printf("鸡有%d只，兔有%d只",j,t);
		}
	}

}
